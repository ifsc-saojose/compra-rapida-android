package com.example.beatriz.listatcc.Model;

public class SharedWith {

    public String email;

    public String name;

    public SharedWith() {

    }

    public SharedWith(String email, String name) {
        this.email = email;
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }


}
